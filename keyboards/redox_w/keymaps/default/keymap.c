layer_state_t layer_state_set_user(layer_state_t state) {
	/*_delay_ms(3000);*/
    switch (get_highest_layer(state)) {
        case _QWERTY:
			/*print("Debug test");*/
			/*dprint("Debug test");*/
            set_led_green;
            break;
        case _SYMB:
            set_led_red;
            break;
        case _NAV:
            set_led_blue;
            break;
        case _ADJUST:
            set_led_red;
            break;
        default:
            break;
    }
  return state;
}

